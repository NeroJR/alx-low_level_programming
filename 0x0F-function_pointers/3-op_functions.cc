#include "3-calc.h"

/**
 *op_add - addition
 *@a: int
 *@b: int
 *Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - subtract
 *@a: int
 *@b: int
 *Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - product
 *@a: int
 *@b: int
 *Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - divides
 *@a: int
 *@b: int
 *Return: quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - remainder of division
 *@a: int
 *@b: int
 *Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
