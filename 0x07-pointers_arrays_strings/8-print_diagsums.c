#include "main.h"
#include <stdio.h>

/**
 *print_diagsums - prints sum of diagonals of square matrix
 *@size: parameter 1
 *@a: parameter 2
 *Return: nothing
 */

void print_diagsums(int *a, int size)
{
	int i;
	int sum1 = 0, sum2 = 0;

	for (i = 0; i < size; i++)
	{
		sum1 += a[i];
		a += size;
	}

	a -= size;

	for (i = 0; i < size; i++)
	{
		sum2 += a[i];
		a -= size;
	}

	printf("%d, %d\n", sum1, sum2);
}
